import { toast } from "react-toastify";

export const toastSucess = (text: string) => {
  toast.success(text, {
    position: "top-right",
    autoClose: 3500,
    hideProgressBar: false,
    closeOnClick: true,
    pauseOnHover: true,
    draggable: true,
    progress: undefined,
    theme: "light",
  });
};
